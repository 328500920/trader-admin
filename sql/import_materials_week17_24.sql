## 五、组合管理

### 1. 定期再平衡

**频率**：每月或每季度

**方法**：
```
1. 计算当前各策略资金占比
2. 与目标占比对比
3. 调整到目标占比
```

**示例**：
```
目标：A:B:C = 40:30:30
当前：A:B:C = 50:25:25
操作：从A转移资金到B和C
```

### 2. 动态调整

**根据市场状态调整**：
```
趋势市场：
- 增加趋势策略权重
- 减少震荡策略权重

震荡市场：
- 增加震荡策略权重
- 减少趋势策略权重
```

**根据策略表现调整**：
```
策略连续亏损：
- 减少该策略权重
- 或暂停该策略

策略持续盈利：
- 可适当增加权重
- 但不要过度集中
```

### 3. 风险监控

**监控指标**：
- 组合整体回撤
- 各策略相关性变化
- 单一策略占比

**风险控制**：
- 组合最大回撤 < 20%
- 单一策略占比 < 50%
- 相关性过高时调整

