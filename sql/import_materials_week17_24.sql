## 三、实盘心理调适

### 1. 常见心理问题

**害怕亏损**：
- 表现：不敢入场，过早止盈
- 原因：真金白银的压力
- 解决：接受亏损是交易的一部分

**急于求成**：
- 表现：重仓、频繁交易
- 原因：想快速赚钱
- 解决：设定合理预期

**患得患失**：
- 表现：盯盘、情绪波动
- 原因：对结果过度关注
- 解决：关注过程而非结果

### 2. 心理调适方法

**设定合理预期**：
- 第一年目标：不亏或小亏
- 月收益预期：5-10%
- 接受波动和回撤

**建立交易routine**：
- 固定时间分析市场
- 固定时间复盘
- 不要全天盯盘

**保持生活平衡**：
- 交易只是生活的一部分
- 保持其他兴趣爱好
- 不要让交易影响生活

