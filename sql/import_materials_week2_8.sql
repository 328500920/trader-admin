'# 支撑阻力位识别与交易策略

## 一、支撑位与阻力位的本质

### 1. 什么是支撑位

**定义**：价格下跌过程中，买方力量集中的价格区域，能够阻止价格继续下跌。

**形成原因**：
- 前期低点：交易者记忆中的"便宜价格"
- 整数关口：心理价位（如BTC的50000、60000）
- 均线位置：MA20、MA50、MA200等
- 斐波那契回撤位：38.2%、50%、61.8%

### 2. 什么是阻力位

**定义**：价格上涨过程中，卖方力量集中的价格区域，能够阻止价格继续上涨。

**形成原因**：
- 前期高点：套牢盘解套卖出
- 整数关口：获利了结心理
- 下降趋势线：技术性卖压
- 前期成交密集区：筹码堆积

---

## 二、识别支撑阻力的方法

### 1. 水平支撑阻力

**识别要点**：
- 找出价格多次触及但未突破的水平线
- 触及次数越多，该位置越重要
- 时间跨度越长，有效性越强

**有效性评估**：
| 触及次数 | 时间跨度 | 有效性 |
|---------|---------|--------|
| 2次 | < 1周 | 弱 |
| 3次 | 1-4周 | 中 |
| 4次+ | > 1月 | 强 |

### 2. 动态支撑阻力

**常用均线**：
- MA7/MA20：短期支撑阻力
- MA50：中期趋势参考
- MA200：长期牛熊分界线

**使用技巧**：
- 上涨趋势中，均线是支撑
- 下跌趋势中，均线是阻力
- 多条均线汇聚处更有效

### 3. 趋势线支撑阻力

**绘制规则**：
- 上升趋势线：连接两个以上的低点
- 下降趋势线：连接两个以上的高点
- 至少需要3个触点才算有效

