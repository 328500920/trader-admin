## 四、常见问题诊断

### 1. 胜率低的原因

**可能原因**：
- 逆势交易
- 入场时机不好
- 止损设置太紧

**解决方案**：
- 只顺势交易
- 等待回调入场
- 给止损更多空间

### 2. 盈亏比低的原因

**可能原因**：
- 过早止盈
- 止损太宽
- 不让利润奔跑

**解决方案**：
- 使用移动止损
- 优化止损位置
- 培养持仓耐心

### 3. 频繁止损的原因

**可能原因**：
- 震荡市使用趋势策略
- 止损设置太紧
- 入场点位不好

**解决方案**：
- 识别市场状态
- 根据ATR设置止损
- 等待更好的入场点

### 4. 大亏的原因

**可能原因**：
- 没有止损
- 仓位太重
- 逆势加仓

**解决方案**：
- 严格执行止损
- 控制仓位大小
- 禁止亏损加仓

