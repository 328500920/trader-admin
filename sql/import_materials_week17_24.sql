## 四、实盘风险控制

### 1. 止损纪律

**铁律**：
- 每笔交易必须设止损
- 止损触发必须执行
- 不要移动止损（除非是移动止盈）

**止损设置**：
- 技术位止损优先
- 最大止损不超过3%
- 考虑滑点因素

### 2. 每日风险限额

**建议设置**：
- 每日最大亏损：总资金的5%
- 达到限额后停止交易
- 第二天再重新开始

**示例**：
```
资金：1000U
每日最大亏损：50U
单笔风险：20U
最多亏损2-3笔后停止
```

### 3. 回撤控制

**回撤限额**：
- 最大回撤：20%
- 达到10%回撤：减少仓位
- 达到20%回撤：暂停交易复盘

