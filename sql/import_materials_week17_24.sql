```
盈利因子 = 总盈利 / 总亏损

评估标准：
- > 1.5：可接受
- > 2：良好
- > 3：优秀
```

**恢复因子（Recovery Factor）**：
```
恢复因子 = 净利润 / 最大回撤

评估标准：
- > 3：良好
- > 5：优秀
