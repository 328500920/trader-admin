## 一、MACD基础原理

### 1. MACD的组成

**DIF线（快线）**：
- 计算：EMA12 - EMA26
- 反映短期与中期趋势的差异
- 快速反应价格变化

**DEA线（慢线）**：
- 计算：DIF的9日EMA
- 对DIF进行平滑处理
- 产生交叉信号

**MACD柱状图**：
- 计算：(DIF - DEA) × 2
- 红柱：DIF > DEA，多头动能
- 绿柱：DIF < DEA，空头动能

### 2. 零轴的意义

**零轴上方**：
- DIF > 0，短期均线在长期均线上方
- 市场处于多头趋势
- 做多为主

**零轴下方**：
- DIF < 0，短期均线在长期均线下方
- 市场处于空头趋势
- 做空或观望

---

## 二、MACD交易信号

### 1. 金叉与死叉

**金叉买入信号**：
- DIF上穿DEA
- 零轴上方金叉：强势买入信号
- 零轴下方金叉：弱势反弹信号

**死叉卖出信号**：
- DIF下穿DEA
- 零轴下方死叉：强势卖出信号
- 零轴上方死叉：弱势回调信号

### 2. 柱状图信号

**红柱变化**：
- 红柱放大：多头动能增强
- 红柱缩小：多头动能减弱
- 红柱转绿：趋势可能反转

**绿柱变化**：
- 绿柱放大：空头动能增强
- 绿柱缩小：空头动能减弱
- 绿柱转红：趋势可能反转

