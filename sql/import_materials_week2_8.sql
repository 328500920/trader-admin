'# RSI与布林带实战指南

## 一、RSI指标详解

### 1. RSI基础知识

**计算公式**：
RSI = 100 - 100/(1 + RS)
RS = N日内上涨平均值 / N日内下跌平均值

**常用参数**：
- RSI(14)：最常用，平衡灵敏度和稳定性
- RSI(7)：更灵敏，适合短线
- RSI(21)：更稳定，适合中长线

### 2. RSI区域划分

| RSI值 | 区域 | 市场状态 |
|------|------|---------|
| > 80 | 极度超买 | 强烈卖出信号 |
| 70-80 | 超买区 | 注意风险 |
| 30-70 | 正常区 | 趋势延续 |
| 20-30 | 超卖区 | 关注买入 |
| < 20 | 极度超卖 | 强烈买入信号 |

### 3. RSI交易信号

**超买超卖策略**：
- RSI > 70 后回落跌破70：卖出信号
- RSI < 30 后反弹突破30：买入信号
- 注意：强势趋势中RSI可能长期超买/超卖

**RSI背离**：
- 价格新高，RSI未新高：顶背离，看跌
- 价格新低，RSI未新低：底背离，看涨
- 背离是RSI最有价值的信号

**RSI中轴线**：
- RSI > 50：多头占优
- RSI < 50：空头占优
- RSI突破/跌破50是趋势确认信号

