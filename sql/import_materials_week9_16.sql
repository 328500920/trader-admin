## 二、趋势的识别

### 1. 道氏理论定义

**上升趋势**：
- 高点不断抬高
- 低点不断抬高
- 形成上升通道

**下降趋势**：
- 高点不断降低
- 低点不断降低
- 形成下降通道

### 2. 均线判断法

**多头趋势**：
- 价格 > MA20 > MA50 > MA200
