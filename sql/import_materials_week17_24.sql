## 三、数据统计与分析

### 1. 基础统计指标

**交易次数统计**：
```
总交易次数：____
盈利次数：____
亏损次数：____
持平次数：____
```

**胜率计算**：
```
胜率 = 盈利次数 / 总交易次数 × 100%
目标：40-60%
```

**盈亏比计算**：
```
平均盈利 = 总盈利 / 盈利次数
平均亏损 = 总亏损 / 亏损次数
盈亏比 = 平均盈利 / 平均亏损
目标：> 1.5
```

**期望值计算**：
```
期望值 = (胜率 × 平均盈利) - (败率 × 平均亏损)
期望值 > 0 才能长期盈利
```

### 2. 进阶分析维度

**按时间分析**：
- 哪个时间段交易效果好
- 周几交易效果好
- 持仓多久效果好

**按品种分析**：
- 哪个品种胜率高
- 哪个品种盈亏比好
- 应该专注哪个品种

**按策略分析**：
- 趋势策略 vs 震荡策略
- 突破策略 vs 回调策略
- 哪种策略更适合自己

**按情绪分析**：
- 情绪好时交易效果
- 情绪差时交易效果
- 情绪对决策的影响

