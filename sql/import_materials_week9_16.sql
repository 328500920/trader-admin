## 二、仓位管理

### 1. 固定金额法

**方法**：每笔交易投入固定金额

**示例**：
- 总资金：10000U
- 每笔投入：1000U（10%）
- 优点：简单易执行
- 缺点：没有考虑止损距离

### 2. 固定比例法（推荐）

**方法**：每笔交易风险固定为总资金的一定比例

**计算公式**：
```
仓位大小 = (总资金 × 风险比例) / (入场价 - 止损价)

示例：
总资金：10000U
风险比例：2%（200U）
入场价：50000
止损价：49000（2%止损）
仓位 = 200 / 1000 = 0.2 BTC
```

**推荐风险比例**：
- 保守：1%
- 标准：2%
- 激进：3%（不推荐超过）

### 3. 凯利公式

**公式**：f = (bp - q) / b

其中：
- f = 最优仓位比例
- b = 盈亏比
- p = 胜率
- q = 败率（1-p）

**示例**：
- 胜率50%，盈亏比2:1
- f = (2×0.5 - 0.5) / 2 = 25%
- 实际使用建议取1/2或1/4凯利值

---

## 三、止损策略

### 1. 固定百分比止损

**方法**：入场价下方固定百分比

**常用设置**：
- 短线：1-2%
- 波段：3-5%
- 长线：5-10%

**优点**：简单明确
**缺点**：可能被正常波动扫损

### 2. 技术位止损

**方法**：根据技术分析设置止损

**常用位置**：
- 前期低点下方
- 支撑位下方
- 均线下方
- 趋势线下方

**优点**：更合理
**缺点**：止损距离不固定

### 3. ATR止损

**方法**：使用ATR（平均真实波幅）设置止损

**计算**：
- 止损 = 入场价 - N × ATR
- N通常取1.5-3

**优点**：适应市场波动
**缺点**：需要计算

### 4. 移动止损（跟踪止损）

**方法**：随着盈利移动止损位

**策略**：
- 盈利1R后，止损移到成本价
- 盈利2R后，止损移到1R位置
- 持续跟踪保护利润

---

## 四、止盈策略

### 1. 固定盈亏比止盈

**方法**：设定固定的盈亏比目标

**常用设置**：
- 保守：1.5:1
- 标准：2:1
- 激进：3:1

### 2. 分批止盈

**方法**：分多次止盈

**示例**：
- 1R时平仓1/3
- 2R时平仓1/3
- 剩余1/3跟踪止损

**优点**：锁定部分利润，保留上涨空间

### 3. 技术位止盈

**方法**：在技术阻力位止盈

**常用位置**：
- 前期高点
- 斐波那契扩展位
- 整数关口

