## 二、小资金实盘策略

### 1. 资金分配

**建议分配**：
- 总资金的10-20%用于实盘
- 其余资金作为后备
- 逐步增加实盘资金

**示例**：
```
总可投资资金：10000U
初始实盘资金：1000U（10%）
后备资金：9000U

第一阶段：1000U实盘
稳定盈利后：增加到2000U
持续稳定后：增加到5000U
```

### 2. 仓位控制

**小资金仓位原则**：
- 单笔风险不超过总资金2%
- 小资金可适当放宽到3%
- 但绝不超过5%

**仓位计算示例**：
```
资金：1000U
单笔风险：2%（20U）
止损距离：2%
仓位 = 20U / 2% = 1000U
实际可用仓位：1000U（满仓）

建议：降低仓位到500U
保留资金应对连续亏损
```

### 3. 交易频率

**建议频率**：
- 每周1-3笔交易
- 只做高概率机会
- 不要过度交易

**原因**：
- 小资金经不起频繁止损
- 手续费占比较高
- 需要精选机会

