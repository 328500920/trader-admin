## 三、MACD背离（核心技术）

### 1. 顶背离（看跌信号）

**定义**：价格创新高，但MACD未创新高

**识别要点**：
- 价格形成更高的高点
- DIF或MACD柱形成更低的高点
- 通常出现在上涨趋势末端

**交易策略**：
- 出现顶背离后减仓或平仓
- 等待MACD死叉确认后做空
- 止损设在新高上方

**成功率**：约65-75%

### 2. 底背离（看涨信号）

**定义**：价格创新低，但MACD未创新低

**识别要点**：
- 价格形成更低的低点
- DIF或MACD柱形成更高的低点
- 通常出现在下跌趋势末端

**交易策略**：
- 出现底背离后关注买入机会
- 等待MACD金叉确认后做多
- 止损设在新低下方

**成功率**：约60-70%

### 3. 隐藏背离

**隐藏顶背离**：
- 价格形成更低的高点
- MACD形成更高的高点
- 预示下跌趋势延续

**隐藏底背离**：
- 价格形成更高的低点
- MACD形成更低的低点
- 预示上涨趋势延续

---

## 四、MACD实战技巧

### 1. 多周期MACD分析

**周线MACD**：
- 判断大趋势方向
- 金叉做多，死叉做空

**日线MACD**：
