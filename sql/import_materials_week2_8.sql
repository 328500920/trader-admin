## 一、均线基础知识

### 1. SMA vs EMA

**简单移动平均线（SMA）**：
- 计算方式：N日收盘价之和 ÷ N
- 特点：平滑，反应较慢
- 适用：判断长期趋势

**指数移动平均线（EMA）**：
- 计算方式：给近期价格更高权重
- 特点：灵敏，反应较快
- 适用：短期交易信号

### 2. 常用均线周期

| 周期 | 用途 | 适用场景 |
|-----|------|---------|
| MA7 | 超短期趋势 | 日内交易 |
| MA20 | 短期趋势 | 波段交易 |
| MA50 | 中期趋势 | 趋势跟踪 |
| MA100 | 中长期趋势 | 趋势确认 |
| MA200 | 长期趋势 | 牛熊分界 |

