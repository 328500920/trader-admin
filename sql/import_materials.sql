'# 常见的K线形态及其价格走势分析

## 一、单根K线形态

### 1. 锤子线（Hammer）

**形态特征**：下影线长度至少是实体的2倍，上影线很短或没有，出现在下跌趋势末端。

**价格走势**：
- 出现后通常预示下跌趋势可能反转
- 多头开始入场，价格有望上涨
- 需要后续阳线确认

**成功率**：约60-65%（需配合成交量确认可提高至70%）

---

### 2. 倒锤子线（Inverted Hammer）

**形态特征**：上影线长度至少是实体的2倍，下影线很短或没有，出现在下跌趋势末端。

**价格走势**：
- 表明买方尝试推高价格
- 若次日高开，确认反转信号
- 可能开启上涨行情

**成功率**：约55-60%

---

### 3. 上吊线（Hanging Man）

**形态特征**：与锤子线形态相同，但出现在上涨趋势末端。

**价格走势**：
- 预示上涨动能减弱
- 空头开始试探性打压
- 可能出现趋势反转下跌

**成功率**：约55-60%

---

### 4. 射击之星（Shooting Star）

**形态特征**：上影线长，实体小，下影线很短或没有，出现在上涨趋势末端。

**价格走势**：
- 多头冲高后被空头打压
- 强烈的见顶信号
- 后续大概率下跌

**成功率**：约65-70%

---

### 5. 十字星（Doji）

**形态特征**：开盘价与收盘价几乎相等，形成十字形状。

**价格走势**：
- 表示多空力量均衡
- 趋势可能发生转变
- 需结合位置和后续K线判断方向

**成功率**：约50%（单独使用意义不大，需配合其他信号）

---

### 6. 大阳线/大阴线

**形态特征**：实体很长，影线很短。

**价格走势**：
- 大阳线：强烈看涨信号，多头占绝对优势
- 大阴线：强烈看跌信号，空头占绝对优势
- 通常预示趋势延续

**成功率**：约70-75%（趋势延续）

---

## 二、双K线组合形态

### 7. 看涨吞没（Bullish Engulfing）

**形态特征**：第二根阳线实体完全包住第一根阴线实体，出现在下跌趋势中。

**价格走势**：
- 强烈的反转信号
- 多头力量压倒空头
- 后续上涨概率高

**成功率**：约63-68%

---

### 8. 看跌吞没（Bearish Engulfing）

**形态特征**：第二根阴线实体完全包住第一根阳线实体，出现在上涨趋势中。

**价格走势**：
- 强烈的见顶信号
- 空头力量压倒多头
- 后续下跌概率高

**成功率**：约65-70%

---

### 9. 乌云盖顶（Dark Cloud Cover）

**形态特征**：第一根为大阳线，第二根阴线高开后收盘价深入阳线实体50%以上。

**价格走势**：
- 上涨趋势中的反转信号
- 多头动能衰竭
- 预示价格将下跌

**成功率**：约60-65%

---

### 10. 刺透形态（Piercing Pattern）

**形态特征**：第一根为大阴线，第二根阳线低开后收盘价深入阴线实体50%以上。

**价格走势**：
- 下跌趋势中的反转信号
- 空头动能衰竭
- 预示价格将上涨

**成功率**：约60-64%

---

### 11. 平头顶部/底部

**形态特征**：两根或多根K线的最高价（顶部）或最低价（底部）几乎相等。

**价格走势**：
- 平头顶部：形成阻力，价格可能下跌
- 平头底部：形成支撑，价格可能上涨

**成功率**：约55-60%

