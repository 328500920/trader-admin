## 二、均线的核心用法

### 1. 趋势判断

**多头排列**：
- MA7 > MA20 > MA50 > MA200
- 价格在所有均线上方
- 表示强势上涨趋势

**空头排列**：
- MA7 < MA20 < MA50 < MA200
- 价格在所有均线下方
- 表示强势下跌趋势

### 2. 金叉与死叉

**金叉（买入信号）**：
- 短期均线上穿长期均线
- 常用组合：MA7上穿MA20、MA20上穿MA50
- 在零轴上方的金叉更可靠

**死叉（卖出信号）**：
- 短期均线下穿长期均线
- 常用组合：MA7下穿MA20、MA20下穿MA50
- 在零轴下方的死叉更可靠

### 3. 均线支撑与阻力

**上涨趋势中**：
- 均线是动态支撑
- 价格回踩均线是买入机会
- MA20是最常用的回踩均线

**下跌趋势中**：
- 均线是动态阻力
- 价格反弹到均线是卖出机会
- 反弹不过均线继续看跌

