## 四、资金分配方法

### 1. 等权重分配

**方法**：每个策略分配相同资金

**示例**：
```
总资金：10000U
策略A：3333U
策略B：3333U
策略C：3334U
```

**优点**：简单
**缺点**：没有考虑策略表现差异

### 2. 风险平价分配

**方法**：根据策略波动率分配，使每个策略贡献相同风险

**示例**：
```
策略A波动率：10%
策略B波动率：20%
策略C波动率：15%

分配比例：
策略A：40%
策略B：20%
策略C：26.7%
```

**优点**：风险更均衡
**缺点**：计算复杂

### 3. 表现加权分配

**方法**：表现好的策略分配更多资金

**示例**：
```
策略A夏普比率：2.0
策略B夏普比率：1.5
策略C夏普比率：1.0

分配比例：
策略A：44%
策略B：33%
策略C：22%
```

**优点**：优化收益
**缺点**：可能过度集中

