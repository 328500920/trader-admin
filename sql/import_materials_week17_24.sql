'# 多策略组合与资产配置

## 一、策略组合的意义

### 1. 为什么需要多策略

**单一策略的问题**：
- 在某些市场状态下失效
- 收益曲线波动大
- 心理压力大

**多策略的优势**：
- 适应不同市场状态
- 平滑收益曲线
- 降低整体风险

### 2. 策略组合的目标

**主要目标**：
- 降低最大回撤
- 提高夏普比率
- 稳定收益

**次要目标**：
- 减少空仓时间
- 提高资金利用率
- 分散风险

---

## 二、策略相关性分析

### 1. 什么是策略相关性

**定义**：两个策略收益之间的关联程度

**相关系数**：
- +1：完全正相关（同涨同跌）
- 0：不相关
- -1：完全负相关（一涨一跌）

### 2. 理想的策略组合

**低相关性组合**：
- 策略A盈利时，策略B可能亏损
- 策略A亏损时，策略B可能盈利
- 整体收益更稳定

**组合示例**：
```
策略A：趋势跟踪（趋势市盈利）
策略B：均值回归（震荡市盈利）
相关性：低或负相关
组合效果：无论趋势还是震荡都有策略盈利
```

### 3. 相关性计算

**简单方法**：
- 观察两个策略的收益曲线
- 是否同涨同跌
- 主观判断相关性

**量化方法**：
- 计算收益的相关系数
- 使用Excel或Python
- 相关系数 < 0.3 为低相关

---

## 三、常见策略组合

### 1. 趋势 + 震荡组合

**策略A：趋势跟踪**
- 适用：趋势市场
- 特点：胜率低，盈亏比高

**策略B：区间交易**
- 适用：震荡市场
- 特点：胜率高，盈亏比低

**组合效果**：
- 趋势市：策略A盈利，策略B小亏
- 震荡市：策略B盈利，策略A小亏
- 整体：收益更稳定

### 2. 多周期组合

**策略A：日线趋势**
- 持仓时间：数天到数周
- 交易频率：低

**策略B：4小时波段**
- 持仓时间：数小时到数天
- 交易频率：中

**策略C：1小时短线**
- 持仓时间：数小时
- 交易频率：高

**组合效果**：
- 不同时间维度捕捉机会
- 提高资金利用率

### 3. 多品种组合

**策略应用于多个品种**：
- BTC趋势策略
- ETH趋势策略
- 其他主流币策略

**组合效果**：
- 分散单一品种风险
- 捕捉不同品种机会

