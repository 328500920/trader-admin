|-----|------|---------|
| 市场风险 | 价格波动带来的亏损 | 止损、仓位控制 |
| 流动性风险 | 无法按预期价格成交 | 选择流动性好的品种 |
| 系统性风险 | 整个市场崩盘 | 分散投资、控制总仓位 |
| 操作风险 | 人为失误 | 规范操作流程 |
| 黑天鹅风险 | 极端事件 | 永远不要满仓 |

