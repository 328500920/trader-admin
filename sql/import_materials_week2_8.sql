## 四、交易策略

### 1. 支撑位做多策略

**入场条件**：
- 价格回落到重要支撑位
- 出现看涨K线形态（锤子线、吞没等）
- 成交量萎缩后放量

**止损设置**：支撑位下方1-2%

**止盈目标**：前期高点或下一阻力位

### 2. 阻力位做空策略

**入场条件**：
- 价格上涨到重要阻力位
- 出现看跌K线形态（射击之星、吞没等）
- 上涨动能减弱

**止损设置**：阻力位上方1-2%

**止盈目标**：前期低点或下一支撑位

### 3. 突破交易策略

**有效突破特征**：
- 收盘价突破（非影线突破）
- 突破时成交量放大
- 突破幅度超过1-3%

**假突破识别**：
- 仅影线突破，收盘回到区间内
- 突破时成交量萎缩
- 快速回落到原区间

---

## 五、实战技巧

### 1. 支撑阻力是区域而非精确点位

- 不要期望价格精确触及某个价格
- 应该将支撑阻力视为一个区域（±1-2%）
- 在区域内分批建仓更安全

### 2. 结合多重确认

- 水平支撑 + 均线支撑 = 更强支撑
- 阻力位 + 斐波那契位 = 更强阻力
- 多重确认提高交易成功率

### 3. 关注突破后的回踩

- 真正的突破往往会有回踩确认
- 回踩不破是最佳入场时机
- 回踩跌破说明是假突破

