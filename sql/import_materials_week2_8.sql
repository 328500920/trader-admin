**四个买入信号**：
1. 均线由下降转为走平或上升，价格从下方突破均线
2. 价格跌破均线后快速回升突破均线
3. 价格在均线上方回落但未跌破均线又上涨
4. 价格跌破均线后远离均线，可能反弹

**四个卖出信号**：
1. 均线由上升转为走平或下降，价格从上方跌破均线
2. 价格突破均线后快速回落跌破均线
3. 价格在均线下方反弹但未突破均线又下跌
