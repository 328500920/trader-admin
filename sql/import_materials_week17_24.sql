**盈亏比（Risk/Reward）**：
```
盈亏比 = 平均盈利 / 平均亏损

评估标准：
- 最低要求：1.5:1
- 良好：2:1以上
- 优秀：3:1以上
```

**期望值（Expectancy）**：
```
期望值 = (胜率 × 平均盈利) - (败率 × 平均亏损)

评估标准：
- 必须 > 0
- 越高越好
```

### 2. 进阶指标

**最大回撤（Max Drawdown）**：
```
最大回撤 = (峰值 - 谷值) / 峰值 × 100%

评估标准：
- 优秀：< 15%
- 良好：15-25%
- 可接受：25-35%
- 需改进：> 35%
```

**夏普比率（Sharpe Ratio）**：
```
夏普比率 = (收益率 - 无风险利率) / 收益标准差

评估标准：
- > 1：可接受
- > 2：良好
- > 3：优秀
```

