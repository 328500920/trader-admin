## 六、组合构建步骤

### 实操流程

```
1. 选择策略
   - 至少2-3个策略
   - 逻辑不同
   - 经过验证

2. 分析相关性
   - 计算历史相关性
   - 选择低相关策略

3. 确定权重
   - 初始等权重
   - 或根据风险分配

4. 回测组合
   - 测试组合表现
   - 对比单一策略

5. 实盘验证
   - 小资金测试
   - 观察实际效果

6. 持续优化
   - 定期再平衡
   - 动态调整
```

