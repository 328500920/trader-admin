## 三、支撑阻力的转换

### 核心原则：突破后角色互换

**支撑变阻力**：
- 价格跌破支撑位后
- 原支撑位变成新的阻力位
- 反弹到该位置容易受阻

**阻力变支撑**：
- 价格突破阻力位后
- 原阻力位变成新的支撑位
- 回踩该位置容易获得支撑

### 实战应用

```
突破买入策略：
1. 价格突破阻力位
2. 等待回踩确认（原阻力变支撑）
3. 在支撑位附近入场做多
4. 止损设在支撑位下方
```

