## 二、持续形态

### 1. 三角形

**对称三角形**：
```
    /\\
   /  \\
  /    \\
 /      \\
/________\\
```

**特点**：
- 高点降低，低点抬高
- 波动逐渐收窄
- 突破方向不确定

**交易方法**：
- 等待突破方向
- 突破后顺势交易
- 目标：三角形最宽处

**上升三角形**：
- 水平阻力 + 上升支撑
- 通常向上突破
- 看涨形态

**下降三角形**：
- 下降阻力 + 水平支撑
- 通常向下突破
- 看跌形态

### 2. 旗形与楔形

**旗形**：
```
上升旗形：
     /|
    / |
   /  |
  /   |
 /    |
旗杆  旗面
```

**特点**：
- 急涨/急跌后的整理
- 旗面与趋势方向相反
- 突破后继续原趋势

**楔形**：
- 类似三角形但倾斜
- 上升楔形看跌
- 下降楔形看涨

### 3. 矩形

**特点**：
- 水平支撑和阻力
- 价格在区间内波动
- 突破后趋势延续

**交易方法**：
- 区间内高抛低吸
- 突破后顺势交易
- 目标：矩形高度

