**双顶（M顶）**：
```
  顶1   顶2
   \\   /
    \\ /
    颈线
```

**识别要点**：
- 两个高点相近
- 中间有明显回调
- 第二个顶部成交量减少

**交易方法**：
- 入场：跌破颈线
- 止损：第二个顶部上方
- 目标：顶部到颈线的距离

**双底（W底）**：
- 形态相反
