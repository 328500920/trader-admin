## 一、经典反转形态

### 1. 头肩顶/头肩底

**头肩顶（看跌）**：
```
形态结构：
    头
   /  \\
左肩    右肩
  \\    /
   颈线
```

**识别要点**：
- 左肩、头、右肩依次形成
- 头部最高，两肩相对对称
- 颈线连接两个低点

**交易方法**：
- 入场：跌破颈线后做空
- 止损：右肩高点上方
- 目标：头部到颈线的距离

**成功率**：约70%

**头肩底（看涨）**：
- 形态相反
- 突破颈线做多
