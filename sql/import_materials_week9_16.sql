## 五、资金管理规则

### 1. 单笔风险控制

- 单笔亏损不超过总资金2%
- 连续亏损后降低仓位
- 盈利后可适当增加仓位

### 2. 总仓位控制

- 总仓位不超过30-50%
- 单一品种不超过20%
- 保留现金应对机会

### 3. 回撤控制

- 设定最大回撤线（如20%）
- 达到回撤线暂停交易
- 复盘后再重新开始

### 4. 盈利保护

- 盈利后提取部分利润
- 不要把所有盈利再投入
- 建立安全垫

