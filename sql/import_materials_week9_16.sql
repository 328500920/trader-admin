**空头趋势**：
- 价格 < MA20 < MA50 < MA200
- 均线空头排列
- 均线向下发散

### 3. 趋势线判断法

**上升趋势线**：
- 连接两个以上低点
- 价格在趋势线上方运行
- 趋势线被有效跌破则趋势结束

**下降趋势线**：
- 连接两个以上高点
- 价格在趋势线下方运行
- 趋势线被有效突破则趋势结束

