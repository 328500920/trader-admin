## 二、布林带详解

### 1. 布林带组成

**中轨**：20日简单移动平均线（MA20）
**上轨**：中轨 + 2倍标准差
**下轨**：中轨 - 2倍标准差

**带宽**：(上轨 - 下轨) / 中轨
- 带宽收窄：波动率降低，即将突破
- 带宽扩张：波动率增加，趋势形成

### 2. 布林带形态

**收口形态（Squeeze）**：
- 上下轨距离缩小
- 表示市场处于盘整
- 预示即将出现大行情

**开口形态**：
- 上下轨距离扩大
- 表示趋势正在形成
- 顺势交易的好时机

**喇叭口形态**：
- 上下轨急剧扩张
- 表示波动剧烈
- 注意风险控制

### 3. 布林带交易策略

**突破策略**：
- 价格突破上轨：可能开启上涨趋势
- 价格跌破下轨：可能开启下跌趋势
- 需要成交量配合确认

**回归策略**：
- 价格触及上轨后回落：短线卖出
- 价格触及下轨后反弹：短线买入
- 适用于震荡市场

**中轨策略**：
- 价格站上中轨：看多
- 价格跌破中轨：看空
- 中轨是趋势分界线

---

## 三、RSI + 布林带组合策略

### 1. 超卖 + 下轨买入

**入场条件**：
- RSI < 30（超卖）
- 价格触及布林带下轨
- 出现看涨K线形态

**止损**：下轨下方1-2%
**止盈**：中轨或上轨

### 2. 超买 + 上轨卖出

**入场条件**：
- RSI > 70（超买）
- 价格触及布林带上轨
- 出现看跌K线形态

**止损**：上轨上方1-2%
**止盈**：中轨或下轨

### 3. 背离 + 布林带确认

**底部信号**：
- RSI底背离
- 价格在布林带下轨附近
- 布林带开始收口

**顶部信号**：
- RSI顶背离
- 价格在布林带上轨附近
